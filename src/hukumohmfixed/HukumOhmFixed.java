    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (null != rumusfisika.getSelectedItem().toString()) switch (rumusfisika.getSelectedItem().toString()) {
            case "Rumus Tegangan Listrik":
                new Tegangan().setVisible(true);
                dispose();
                break;
            case "Rumus Hambatan Listrik":
                new Hambatan().setVisible(true);
                dispose();
                break;
            case "Rumus Kuat Arus Listrik":
                new Kuatarus().setVisible(true);
                dispose();
                break;
            default:
                break;
        }

    }//GEN-LAST:event_jButton1ActionPerformed

